var i = 0;
var j = 20;

while(i<=20) {
  if(i===0){
    console.log('LOOPING PERTAMA');
  } else {
    console.log(i + ' I love coding')
  }
  i+=2;
}

while(j>0) {
  if(j===20){
    console.log('LOOPING KEDUA' + '\n' + j + ' I will become fullstack developer')
  } else {
    console.log(j + ' I will become fullstack developer')
  }
  j-=2;
}
